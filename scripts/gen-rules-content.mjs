#!/usr/bin/env node
/**
 * Generate apps/web/src/lib/rules-content.ts from rules-content.data.json — the
 * "why it matters" + bad→good example pairs that power the /rules reference page.
 *
 * The data is correctness-checked by apps/web/tests/unit/rules-content.test.ts:
 * every `bad` example must trigger its rule and every `good` example must pass.
 *
 * Usage: `npm run gen:rules-content`.
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const here = path.dirname(fileURLToPath(import.meta.url));
const DATA = path.join(here, '..', 'apps', 'web', 'src', 'lib', 'rules-content.data.json');
const OUT = path.join(here, '..', 'apps', 'web', 'src', 'lib', 'rules-content.ts');

const entries = JSON.parse(fs.readFileSync(DATA, 'utf8'));
const map = {};
for (const e of entries) {
  const o = { whyItMatters: e.whyItMatters, tip: e.tip, demoable: !!e.demoable };
  if (e.demoable && e.bad != null && e.badKind) o.bad = { kind: e.badKind, content: e.bad };
  if (e.demoable && e.good != null && e.goodKind) o.good = { kind: e.goodKind, content: e.good };
  map[e.ruleId] = o;
}

const ts =
  '// AUTO-GENERATED by scripts/gen-rules-content.mjs from rules-content.data.json — do not edit by hand.\n' +
  '// Every bad example triggers its rule and every good example passes\n' +
  '// (asserted in apps/web/tests/unit/rules-content.test.ts). Run: npm run gen:rules-content\n' +
  "import type { SelectableKind } from './detect-kind';\n\n" +
  'export interface RuleContent {\n' +
  '  whyItMatters: string;\n' +
  '  tip: string;\n' +
  '  demoable: boolean;\n' +
  '  bad?: { kind: SelectableKind; content: string };\n' +
  '  good?: { kind: SelectableKind; content: string };\n' +
  '}\n\n' +
  'export const RULE_CONTENT: Record<string, RuleContent> = ' +
  JSON.stringify(map, null, 2) +
  ';\n';

fs.writeFileSync(OUT, ts);
console.log('wrote', path.relative(process.cwd(), OUT), Object.keys(map).length, 'rules');
